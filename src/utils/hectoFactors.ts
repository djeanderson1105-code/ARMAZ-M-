/**
 * Returns the Hectoliter (HL) factor for a given product code
 */
export function getHectoFactor(productCode: string): number {
  const codeStr = String(productCode).trim();
  
  // Check the spreadsheet-based PRODUCT_DATABASE first for maximum accuracy
  const matchInDb = PRODUCT_DATABASE.find(p => p.codigo === codeStr);
  if (matchInDb) {
    return matchInDb.fatorHecto;
  }
  const matchInDbClean = PRODUCT_DATABASE.find(p => p.codigo === codeStr.replace(/^0+/, ""));
  if (matchInDbClean) {
    return matchInDbClean.fatorHecto;
  }

  if (PRODUCT_HECTO_FACTORS[codeStr] !== undefined) {
    return PRODUCT_HECTO_FACTORS[codeStr];
  }
  // Try fallback of removing leading/trailing zeros or spaces
  const cleanCode = codeStr.replace(/^0+/, "");
  if (PRODUCT_HECTO_FACTORS[cleanCode] !== undefined) {
    return PRODUCT_HECTO_FACTORS[cleanCode];
  }
  
  // Default fallback factor if code is unknown
  return 0.05;
}

/**
 * Calculates Hectoliters (HL) for a physical quantity of a product
 */
export function calculateHL(productCode: string, quantity: number): number {
  const factor = getHectoFactor(productCode);
  return Number((quantity * factor).toFixed(5));
}
